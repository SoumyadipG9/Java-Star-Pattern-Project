package book;

public class Nested_For_While_Loop {

	public static void main(String[] args) {
		
		for(int c=1; c<=10; c++)
		{
			for(int i=1; i<=6; i++)                           //Nested for loop.
			{
				System.out.print(i +" ");
			}
			System.out.println(" Row" + c);
			System.out.println();
			
		}
		
		

	}

}
