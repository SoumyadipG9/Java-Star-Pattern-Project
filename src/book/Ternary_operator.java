package book;

public class Ternary_operator {

	public static void main(String[] args) {
		
		int a=35;
		int b=95;
		int c=65;
		
		                                                        //This Is  Ternary Operator Program.
		
		int max= a>b ? a>c ? a:c : b>c ?  b:c ;

		System.out.println(max);
	}

}
