package Practices;

public class Arithmetic_operators_1 {

	public static void main(String[] args) {
		
		int a=6;
		int b=4;
		
		int sum1=a+b;
		int sum2=a-b;
		int sum3=a*b;
		int sum4=a/b;
		int sum5=a%b;
		
		System.out.println("Sum of two numbers is: "+sum1);
		System.out.println("Sub of two numbers is: "+sum2);
		System.out.println("Multi of two numbers is: "+sum3);
		System.out.println("Div of two numbers is: "+sum4);
		System.out.println("Mod of two numbers is: "+sum5);

	}

}
